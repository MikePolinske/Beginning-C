/* 
 * Exercise 1-1 Write a program that will output your name and address
 * using a separate printf() statement for each line of output.
 */

#include <stdio.h>
int main(void)
{
    printf("My name is:\tMichael Polinske\n");
    printf("My address is:\t5730 North 68th Street\n");
    printf("\t\tMilwaukee, WI 53218\n");
    return 0;
}